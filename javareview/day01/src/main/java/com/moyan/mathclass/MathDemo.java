public class MathDemo {

    public static void main(String[] args) {
    //Math : 包含了一些基本的数学运算方法
        /*static double PI (π数学符号)
        System.out.println(Math.PI);*/

        /*static double abs(double a) : 返回绝对值
        System.out.println(Math.abs(15));
        System.out.println(Math.abs(-10));*/

        /*static double ceil(double a) : ceil(天花板) 向上取整
        System.out.println(Math.ceil(1.2)); = 2
        System.out.println(Math.ceil(1.6)); = 2


        static double floor(double a) ： floor(地板) 向下取整数
        System.out.println(Math.floor(1.2)); = 1
        System.out.println(Math.floor(1.9)); =1
        */

        /*static long round(double a) : 四舍五入
        System.out.println(Math.round(1.2)); 小于五向下取整
        System.out.println(Math.round(1.6)); 大于五向上取整
        */

        /*static double max(double a, double b) ： 取最大值
        System.out.println(Math.max(10,23));
        */

        /*static double pow(double a) : 返回参数第一个参数的第二个参数次幂 1*3*3=9
        System.out.println(Math.pow(3,2));*/

        /*static double random(double a) : 返回一个随机数，大于零且小于一的
        System.out.println(Math.random());*/
    }
}
